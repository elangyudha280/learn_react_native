//!2. kenapa menggunakan expo
/*
    * Kemudahan Setup = denga expo kita tidak perlu melakukan configurasi tambahan seperti menginstal Android SDK, Xcode, java jdk dll

    * Hot Reloading = memungkinkan kita merefresh atau load perubahan secara langsung tanpa perlu menjalankan ulang

   * Akses ke API dan Pustaka = memiliki fungsi" tambahan seperti  akses ke kamera, lokasi, penyimpanan, dan notifikasi, yang dapat digunakan tanpa konfigurasi tambahan.

    * Dukungan Cross-Platform = dengan expo kita bisa menjalankan aplikasi react native di IOS tanpa perlu xcode 
*/

